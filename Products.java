    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        if (!prd_id.getText().isEmpty() && !prd_name.getText().isEmpty() && !retailprice.getText().isEmpty() && !wholesaleprice.getText().isEmpty() && !stock.getText().isEmpty()) {
            int productId = Integer.parseInt(prd_id.getText().trim());
            String productName = prd_name.getText().trim();
            double retailPrice = Double.parseDouble(retailprice.getText().trim());
            double wholesalePrice = Double.parseDouble(wholesaleprice.getText().trim());
            int stockValue = Integer.parseInt(stock.getText().trim());
            
            boolean idExists = false;
            for (Product product : Store.getProducts()) {
                if (product.getProductId() == productId) {
                    idExists = true;
                    break;
                }
            }
            if (idExists) {
                JOptionPane.showMessageDialog(this, "A product with this ID already exists. Please use a different ID.", "Duplicate ID Error", JOptionPane.ERROR_MESSAGE);
            }
            
            else if (stockValue > 0) {
                Product p = new Product();
                p.setProductId(productId);
                p.setProductName(productName);
                p.setRetailPrice(retailPrice);
                p.setWholesalePrice(wholesalePrice);
                p.setStock(stockValue);
                
                Store.addProduct(p);
                
                refreshTable();
                JOptionPane.showMessageDialog(null, "Product added successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Stock value must be greater than 0.", "Input Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        try {
            int productId = Integer.parseInt(prd_id.getText().trim());
            String productName = prd_name.getText().trim();
            double retailPrice = Double.parseDouble(retailprice.getText().trim());
            double wholesalePrice = Double.parseDouble(wholesaleprice.getText().trim());
            int stockAmount = Integer.parseInt(stock.getText().trim());

            if (stockAmount > 0){
                Product updatedProduct = new Product(productId, productName, retailPrice, wholesalePrice, stockAmount);

                Store.updateProduct(updatedProduct);
                refreshTable();

                JOptionPane.showMessageDialog(this, "Product updated successfully!");
            }
            else{
                JOptionPane.showMessageDialog(this, "Stock value must be greater than 0.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid product data.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateBtnActionPerformed

    private void delbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delbtnActionPerformed
        try {
            if (prd_id.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Product ID cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            String productIdStr = prd_id.getText().trim();
            if (!productIdStr.matches("\\d+")) {
                throw new NumberFormatException("Product ID must be a valid integer.");
            }
            int productId = Integer.parseInt(productIdStr);

            Store.deleteProduct(productId, "products.txt");
            refreshTable();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Input Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_delbtnActionPerformed

