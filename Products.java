    public void setTableValue(ArrayList<Product> objects) {
        if (objects != null) {
            DefaultTableModel model = (DefaultTableModel) productlist.getModel();
            model.setRowCount(0);
            for (int i = 0; i < objects.size(); i++) {
                Object[] columns = new Object[5];
                columns[0] = objects.get(i).getProductName();
                columns[1] = objects.get(i).getProductId();
                columns[2] = objects.get(i).getRetailPrice();
                columns[3] = objects.get(i).getWholesalePrice();
                columns[4] = objects.get(i).getStock();
                model.addRow(columns);
            }
        }
    }
    
      void setTableValueFromFile(String filePath) {
        ArrayList<Product> products = Store.loadProductsFromFile(filePath);
        setTableValue(products);
    }



    public void refreshTable() {
    ArrayList<Product> allProducts = Store.loadProductsFromFile("products.txt");
    setTableValue(allProducts);
}
