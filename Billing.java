    private void soldItemsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_soldItemsActionPerformed
        String selectedProductName = (String) soldItems.getSelectedItem();

        for (Sale sale : sales) {
            if (sale.getProduct() != null && sale.getProduct().getProductName().equals(selectedProductName)) {
                quantityPurchased.setText(String.valueOf(sale.getQuantityPurchased()));
                wholeSalePrice.setText(String.valueOf(sale.getProduct().getWholesalePrice()));
                actualAmount.setText(String.valueOf(sale.getProduct().getRetailPrice()));
                return;
            }
        }
        quantityPurchased.setText("");
        wholeSalePrice.setText("");
    }//GEN-LAST:event_soldItemsActionPerformed

