    private void calculateBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateBillActionPerformed
        double totalbill = 0.0;
        double totalCost = 0.0; 
        for (Sale sale : sales) {
            Product product = sale.getProduct();
            int quantity = sale.getQuantityPurchased();

            double productAmount = product.getRetailPrice() * quantity;
            double productCost = product.getWholesalePrice() * quantity;
            totalbill += productAmount;
            totalCost += productCost;

            soldItems.removeItem(product.getProductName());
        }
        Customer customer = sales.get(0).getCustomer(); 
        if (customer.isCustomerType() == true) {
            totalbill *= 0.85;
        }
        double amountreceived = Double.parseDouble(amountReceived.getText());
        String amountReceivedText = amountReceived.getText();
        if (amountReceivedText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Error: Amount received field is empty.", "Input Error", JOptionPane.ERROR_MESSAGE);
        }
        
        else if (amountreceived < totalbill) {
            JOptionPane.showMessageDialog(this, "Error: Amount received is less than the total bill.", "Payment Error", JOptionPane.ERROR_MESSAGE);
        }
        
        else{
            totalBill.setText(String.format("%.2f", totalbill));
            double profit = totalbill - totalCost;
            double Loss = totalCost - totalbill;
            if (totalCost > totalbill) {
                Profit.setText(String.format("%.2f", profit));
                loss.setText("0.00");
            } else {
                Profit.setText("0.00");
                loss.setText(String.format("%.2f", Loss));
            }
        }

    }//GEN-LAST:event_calculateBillActionPerformed

