    private void loadSelectedSales(ArrayList<Sale> sales) {
        if (soldItems == null) {
            System.out.println("soldItems combo box is not initialized.");
            return;
        }

        soldItems.removeAllItems();

        if (sales == null || sales.isEmpty()) {
            System.out.println("No sales data available.");
            return;
        }

        for (Sale sale : sales) {
            if (sale.getProduct() != null) {
                String productName = sale.getProduct().getProductName();
                if (productName != null) {
                    soldItems.addItem(productName);
                } else {
                    System.out.println("Product name is null.");
                }
            } else {
                System.out.println("Sale product is null.");
            }
        }
    }

