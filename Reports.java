    private void ReportsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReportsActionPerformed
        String selectedProductName = (String) selectedProduct.getSelectedItem();
        if (selectedProductName == null || selectedProductName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a product.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return;
        }
        selectedProductName = selectedProductName.trim().toLowerCase();
        int totalUnitsSold = 0;
        boolean productFound = false;

        try (BufferedReader reader = new BufferedReader(new FileReader("sales.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(", ");
                String productName = parts[0].split(": ")[1].trim().toLowerCase();
                int quantity = Integer.parseInt(parts[5].split(": ")[1]);
                if (productName.equals(selectedProductName)) {
                    totalUnitsSold += quantity;
                    productFound = true;
                }
            }
        } catch (IOException e) {
            System.err.println("Error reading sales data: " + e.getMessage());
        }
        if (productFound) {
            unitsSold.setText(String.valueOf(totalUnitsSold));
        } else {
            unitsSold.setText("0");
            JOptionPane.showMessageDialog(this, "No units of the selected product were sold.", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_ReportsActionPerformed

