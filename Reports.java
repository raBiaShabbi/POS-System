    private void getReportsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getReportsActionPerformed
   
        String monthStr = month.getText().trim();
        String yearStr = year.getText().trim();
        int monthValue;
        int yearValue;

        try {
            monthValue = Integer.parseInt(monthStr);
            yearValue = Integer.parseInt(yearStr);

            if (monthValue < 1 || monthValue > 12) {
                JOptionPane.showMessageDialog(this, "Please enter a valid month (1-12).", "Invalid Input", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please enter valid numeric values for month and year.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return;
        }

        double totalrevenue = 0;
        int totaltransactions = 0;
        int totalUnitsSold = 0;
        String selectedProductName = (String) selectedProduct.getSelectedItem();

        try (BufferedReader reader = new BufferedReader(new FileReader("sales.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(", ");

                String saleDate = parts[6].split(": ")[1]; 
                double retailPrice = Double.parseDouble(parts[4].split(": ")[1]);
                int quantity = Integer.parseInt(parts[5].split(": ")[1]);

                LocalDate saleLocalDate = LocalDate.parse(saleDate, DateFormatter);

                if (saleLocalDate.getMonthValue() == monthValue && saleLocalDate.getYear() == yearValue) {
                    totalrevenue += retailPrice * quantity;
                    totaltransactions++;  
                }
            }
        } catch (IOException e) {
            System.err.println("Error reading sales data: " + e.getMessage());
        }

        totalRevenue.setText(String.valueOf(totalrevenue));
        totalTransactions.setText(String.valueOf(totaltransactions));
        unitsSold.setText(String.valueOf(totalUnitsSold));   
    }//GEN-LAST:event_getReportsActionPerformed

